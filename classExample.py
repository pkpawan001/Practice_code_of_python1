
# Finding Area of rectangle 
class Rectengle:
    def __init__(self):
        self.length=10
        self.Breath=20
    def area(self):
        return self.length*self.Breath
    
r=Rectengle()
print("Length of R=",r.length)
print("Breath of R=",r.Breath)
print("Area of rectangle:",r.area())
print(r.length)
